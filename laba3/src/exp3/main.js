"use strict";
class Студент {
    constructor(имя, датаРождения, группа, номерСтудента, среднийБалл) {
        this.имя = имя;
        this.датаРождения = датаРождения;
        this.группа = группа;
        this.номерСтудента = номерСтудента;
        this.среднийБалл = среднийБалл;
    }
    // Метод для вычисления возраста
    получитьВозраст() {
        const сегодня = new Date();
        let возраст = сегодня.getFullYear() - this.датаРождения.getFullYear();
        const месяцРазница = сегодня.getMonth() - this.датаРождения.getMonth();
        if (месяцРазница < 0 || (месяцРазница === 0 && сегодня.getDate() < this.датаРождения.getDate())) {
            возраст--;
        }
        return возраст;
    }
    // Метод для проверки, является ли студент отличником
    этоОтличник() {
        return this.среднийБалл >= 4.8;
    }
    // Метод для форматирования вывода
    описание() {
        return `
      <li>
        Имя: ${this.имя}<br>
        Возраст: ${this.получитьВозраст()}<br>
        Группа: ${this.группа}<br>
        Номер студента: ${this.номерСтудента}<br>
        Средний балл: ${this.среднийБалл}<br>
        Отличник: ${this.этоОтличник() ? "Да" : "Нет"}
      </li>
    `;
    }
}
// Создание объектов студентов
const студент1 = new Студент("Иванов Иван", new Date(2004, 2, 15), "ПИ-22", 1001, 4.9);
const студент2 = new Студент("Петрова Анна", new Date(2003, 10, 5), "ПМИ-21", 1002, 4.3);
// Вывод данных на страницу в виде списка
const вывод = document.getElementById("output");
вывод.innerHTML = `<ol>${студент1.описание()}${студент2.описание()}</ol>`;
